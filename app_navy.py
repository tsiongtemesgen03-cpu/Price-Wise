def make_weekly_chart(fm):
    """Weekly bar chart styled to match the dashboard mockup."""
    days = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun']
    rng = np.random.RandomState(42)

    base = fm['baseline_price'].mean() if len(fm) > 0 else 150
    rec = fm['recommended_price'].mean() if len(fm) > 0 else 200

    day_mult = [0.88, 0.85, 0.87, 0.90, 1.05, 1.30, 1.20]
    current_vals = [base * m + rng.uniform(-3, 3) for m in day_mult]
    recommended_vals = [rec * m + rng.uniform(-3, 3) for m in day_mult]

    x = np.arange(len(days))
    w = 0.38

    fig, ax = plt.subplots(figsize=(9.4, 3.6))
    fig.patch.set_facecolor('white')
    ax.set_facecolor('white')

