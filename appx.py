@st.cache_data
def load_city_data(city):
    if city == "Washington DC":
        df = pd.read_csv("final_dc_airbnb_pricing.csv")
        events = pd.read_csv("dc_events_ticketmaster.csv")
    else:
        df = pd.read_csv("final_chicago_airbnb_pricing.csv")
        events = pd.read_csv("chicago_events_ticketmaster.csv")

    if "action" not in df.columns:
        df["action"] = "HOLD"

    if "recommended_price" not in df.columns:
        if "predicted_price" in df.columns:
            df["recommended_price"] = df["predicted_price"]
        elif "price_clean" in df.columns:
            df["recommended_price"] = df["price_clean"]
        else:
            df["recommended_price"] = np.nan

    for col in [
        "price_clean",
        "recommended_price",
        "predicted_price",
        "review_scores_rating",
        "accommodates",
        "bedrooms",
        "beds",
        "number_of_reviews",
        "nearest_event_distance_miles",
        "latitude",
        "longitude",
    ]:
        if col in df.columns:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    if "recommended_price" in df.columns:
        df["recommended_price"] = df["recommended_price"].clip(upper=1000)

    if "neighbourhood" not in df.columns:
        if "neighbourhood_cleansed" in df.columns:
            df["neighbourhood"] = df["neighbourhood_cleansed"]
        else:
            df["neighbourhood"] = "Unknown"

    def demand_level(distance):
        if pd.isna(distance):
            return "LOW"
        if distance <= 0.5:
            return "VERY HIGH"
        if distance <= 1.5:
            return "HIGH"
        if distance <= 3:
            return "MEDIUM"
        return "LOW"

    if "nearest_event_distance_miles" in df.columns:
        df["event_demand"] = df["nearest_event_distance_miles"].apply(demand_level)
    else:
        df["event_demand"] = "LOW"

    def why_text(row):
        event_name = row.get("nearest_event_name", "a nearby event")
        dist = row.get("nearest_event_distance_miles", np.nan)
        action = row.get("action", "HOLD")
        if pd.notna(dist):
            return f"{action} because {event_name} is nearby ({dist:.2f} miles away)."
        return f"{action} based on listing and demand characteristics."

    df["why"] = df.apply(why_text, axis=1)

    if "date_local" in events.columns:
        events["date_local"] = pd.to_datetime(events["date_local"], errors="coerce")

    return df, events


