def top_neighborhoods(df, n=6):
    return (
        df.groupby("neighbourhood")["recommended_price"]
        .median()
        .sort_values(ascending=False)
        .head(n)
        .sort_values()
    )


def estimate_uplift(row):
    current_price = row.get("price_clean", np.nan)
    recommended = row.get("recommended_price", np.nan)

    if pd.isna(current_price) or pd.isna(recommended):
        return np.nan

    monthly = (recommended - current_price) * 20
    annual = monthly * 12
    return annual


def compute_financial_impact(df, assumed_margin=0.35, event_nights=3):
    temp = df.copy()

    temp["nightly_uplift"] = temp["recommended_price"] - temp["price_clean"]
    temp["nightly_uplift"] = temp["nightly_uplift"].fillna(0)

    temp["percent_uplift"] = np.where(
        temp["price_clean"].fillna(0) > 0,
        temp["nightly_uplift"] / temp["price_clean"],
        0
    )

    temp["event_period_uplift"] = temp["nightly_uplift"] * event_nights
    temp["monthly_uplift"] = temp["nightly_uplift"] * 20
    temp["annual_uplift"] = temp["monthly_uplift"] * 12
    temp["gross_profit_uplift"] = temp["annual_uplift"] * assumed_margin

    baseline_annual_revenue = (temp["price_clean"].fillna(0) * 20 * 12).sum()
    projected_annual_revenue = (temp["recommended_price"].fillna(0) * 20 * 12).sum()

    summary = {
        "total_nightly_uplift": temp["nightly_uplift"].clip(lower=0).sum(),
        "total_event_period_uplift": temp["event_period_uplift"].clip(lower=0).sum(),
        "total_annual_uplift": temp["annual_uplift"].clip(lower=0).sum(),
        "total_gross_profit_uplift": temp["gross_profit_uplift"].clip(lower=0).sum(),
        "avg_percent_uplift": temp.loc[temp["nightly_uplift"] > 0, "percent_uplift"].mean() * 100 if (temp["nightly_uplift"] > 0).any() else 0,
        "baseline_annual_revenue": baseline_annual_revenue,
        "projected_annual_revenue": projected_annual_revenue,
    }

    neighborhood_view = (
        temp.groupby("neighbourhood")
        .agg(
            avg_current_price=("price_clean", "median"),
            avg_recommended_price=("recommended_price", "median"),
            nightly_uplift=("nightly_uplift", "sum"),
            event_period_uplift=("event_period_uplift", "sum"),
            annual_uplift=("annual_uplift", "sum"),
            gross_profit_uplift=("gross_profit_uplift", "sum"),
        )
        .sort_values("annual_uplift", ascending=False)
        .reset_index()
    )

    return summary, neighborhood_view


def build_price_calendar(selected_listing, city_events):
    current_price = selected_listing.get("price_clean", np.nan)
    listing_distance = selected_listing.get("nearest_event_distance_miles", np.nan)
    listing_demand = selected_listing.get("event_demand", "LOW")
    listing_event_name = selected_listing.get("nearest_event_name", "")

    events = city_events.copy()

    if "date_local" in events.columns:
        events = events.dropna(subset=["date_local"]).sort_values("date_local")

    if listing_event_name and "event_name" in events.columns:
        matched = events[events["event_name"] == listing_event_name].copy()
        others = events[events["event_name"] != listing_event_name].copy()
        events = pd.concat([matched, others], ignore_index=True)

    events = events.head(15).copy()

    def calendar_action(distance, event_type):
        event_type = str(event_type).lower()

        if pd.isna(distance):
            return "HOLD"
        if distance <= 0.5:
            return "RAISE"
        if distance <= 1.5 and event_type in ["music", "sports"]:
            return "RAISE"
        if distance <= 3:
            return "HOLD"
        return "DISCOUNT"

    def calendar_price(base_price, distance, event_type):
        if pd.isna(base_price):
            return np.nan

        event_type = str(event_type).lower()

        if pd.isna(distance):
            return round(base_price, 2)
        if distance <= 0.5:
            uplift = 0.20 if event_type in ["music", "sports"] else 0.12
            return round(base_price * (1 + uplift), 2)
        if distance <= 1.5:
            uplift = 0.12 if event_type in ["music", "sports"] else 0.07
            return round(base_price * (1 + uplift), 2)
        if distance <= 3:
            return round(base_price, 2)
        return round(base_price * 0.95, 2)

    calendar_rows = []

    for _, event in events.iterrows():
        event_name = event.get("event_name", "Unknown event")
        event_type = event.get("event_type", "Unknown")
        event_date = event.get("date_local", pd.NaT)

        distance = listing_distance
        demand = listing_demand

        rec_action = calendar_action(distance, event_type)
        rec_price = calendar_price(current_price, distance, event_type)

        if pd.notna(distance):
            if distance <= 0.5:
                strength = "very strong"
            elif distance <= 1.5:
                strength = "strong"
            elif distance <= 3:
                strength = "moderate"
            else:
                strength = "low"

            why = (
                f"{rec_action} price because {event_name} ({event_type}) is expected to drive {strength} demand. "
                f"This listing is approximately {distance:.2f} miles from the event, increasing booking pressure."
            )
        else:
            why = "HOLD price because no significant nearby event-driven demand is detected for this date."

        calendar_rows.append({
            "Date": event_date,
            "Event": event_name,
            "Event Type": event_type,
            "Distance (miles)": round(distance, 2) if pd.notna(distance) else np.nan,
            "Demand Level": demand,
            "Current Price": round(current_price, 2) if pd.notna(current_price) else np.nan,
            "Recommended Price": rec_price,
            "Action": rec_action,
            "Why": why,
        })

    calendar_df = pd.DataFrame(calendar_rows)

    if not calendar_df.empty and "Date" in calendar_df.columns:
        calendar_df = calendar_df.sort_values("Date")

    return calendar_df


def answer_question(question, df):
    q = question.lower()
    counts = action_counts(df)
    avg_price = df["recommended_price"].mean()

    if "raise" in q:
        top = df[df["action"] == "RAISE"][["name", "neighbourhood", "recommended_price"]].head(5)
        lines = "\n".join(
            f"- {r['name']} | {r['neighbourhood']} | ${r['recommended_price']:.0f}"
            for _, r in top.iterrows()
        )
        return f"{counts['RAISE']:,} listings should raise prices.\n\nTop examples:\n{lines}"

    if "discount" in q:
        return f"{counts['DISCOUNT']:,} listings should discount prices."

    if "hold" in q:
        return f"{counts['HOLD']:,} listings should hold prices."

    if "revenue" in q or "opportunity" in q:
        tmp = df.copy()
        tmp["annual_uplift"] = tmp.apply(estimate_uplift, axis=1)
        total = tmp["annual_uplift"].clip(lower=0).sum()
        return f"Estimated annual revenue opportunity is about ${total:,.0f} across the selected city."

    if "top neighbourhood" in q or "top neighborhood" in q:
        top = top_neighborhoods(df, 5)
        return "Top neighbourhoods by median recommended price:\n" + "\n".join(
            f"- {name}: ${value:,.0f}" for name, value in top.items()
        )

    if "overview" in q:
        return (
            f"There are {len(df):,} listings. "
            f"RAISE: {counts['RAISE']:,}, DISCOUNT: {counts['DISCOUNT']:,}, HOLD: {counts['HOLD']:,}. "
            f"Average recommended price is ${avg_price:,.0f}."
        )

    return "Try asking: which listings should raise prices, what is the revenue opportunity, or top neighbourhoods."



