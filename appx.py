def get_comparable_listings(df, city_neighborhood, room_type, guests, bedrooms):
    comps = df.copy()

    if "neighbourhood" in comps.columns and city_neighborhood != "All":
        comps = comps[comps["neighbourhood"] == city_neighborhood]

    if "room_type" in comps.columns:
        comps = comps[comps["room_type"] == room_type]

    if "accommodates" in comps.columns:
        comps = comps[(comps["accommodates"].fillna(0) - guests).abs() <= 2]

    if "bedrooms" in comps.columns:
        comps = comps[(comps["bedrooms"].fillna(0) - bedrooms).abs() <= 1]

    return comps


def get_confidence_level(event_distance, comp_count):
    if pd.notna(event_distance) and event_distance <= 0.5 and comp_count >= 10:
        return "Very High"
    if pd.notna(event_distance) and event_distance <= 1.5 and comp_count >= 5:
        return "High"
    if pd.notna(event_distance) and event_distance <= 3:
        return "Medium"
    return "Low"


def get_market_position(current_price, market_median):
    if pd.isna(current_price) or pd.isna(market_median):
        return "Unknown"
    if current_price < market_median * 0.9:
        return "Below Market"
    if current_price > market_median * 1.1:
        return "Above Market"
    return "In Line with Market"

def summarize_weather(hourly_df):
    if hourly_df.empty:
        return {
            "avg_temp": np.nan,
            "total_precip": np.nan,
            "max_wind": np.nan,
            "avg_cloud": np.nan,
            "bad_weather_days": 0,
            "weather_score": 50,
        }

    daily = (
        hourly_df.assign(date=pd.to_datetime(hourly_df["time"]).dt.date)
        .groupby("date")
        .agg(
            avg_temp=("temperature_2m", "mean"),
            total_precip=("precipitation", "sum"),
            max_wind=("wind_speed_10m", "max"),
            avg_cloud=("cloud_cover", "mean"),
        )
        .reset_index()
    )

    daily["bad_weather"] = (
        (daily["total_precip"] >= 5) |
        (daily["max_wind"] >= 25)
    )

    avg_temp = daily["avg_temp"].mean()
    total_precip = daily["total_precip"].sum()
    max_wind = daily["max_wind"].max()
    avg_cloud = daily["avg_cloud"].mean()
    bad_weather_days = int(daily["bad_weather"].sum())

    score = 100
    score -= min(total_precip * 2, 30)
    score -= min(bad_weather_days * 10, 30)
    score -= min(max(max_wind - 15, 0), 20)
    score -= min(max((avg_cloud - 70) / 2, 0), 20)
    score = max(0, round(score))

    return {
        "avg_temp": round(avg_temp, 1) if pd.notna(avg_temp) else np.nan,
        "total_precip": round(total_precip, 1) if pd.notna(total_precip) else np.nan,
        "max_wind": round(max_wind, 1) if pd.notna(max_wind) else np.nan,
        "avg_cloud": round(avg_cloud, 1) if pd.notna(avg_cloud) else np.nan,
        "bad_weather_days": bad_weather_days,
        "weather_score": score,
    }

@st.cache_data(show_spinner=False)
def get_weather_for_booking(lat, lon, start_date, end_date):
    start_date = pd.to_datetime(start_date).date()
    end_date = pd.to_datetime(end_date).date()
    today = pd.Timestamp.today().date()

    hourly_vars = [
        "temperature_2m",
        "precipitation",
        "wind_speed_10m",
        "cloud_cover",
        "weather_code",
    ]

    # Open-Meteo forecast only works up to 16 days ahead
    max_forecast_date = today + pd.Timedelta(days=16)

    if end_date < today:
        base_url = "https://archive-api.open-meteo.com/v1/archive"
    elif start_date <= max_forecast_date and end_date <= max_forecast_date:
        base_url = "https://api.open-meteo.com/v1/forecast"
    else:
        # Too far ahead for forecast API
        return pd.DataFrame(), {
            "avg_temp": np.nan,
            "total_precip": np.nan,
            "max_wind": np.nan,
            "avg_cloud": np.nan,
            "bad_weather_days": 0,
            "weather_score": 50,
            "weather_note": "Weather forecast is only available up to 16 days ahead."
        }

    params = {
        "latitude": float(lat),
        "longitude": float(lon),
        "start_date": str(start_date),
        "end_date": str(end_date),
        "hourly": ",".join(hourly_vars),
        "timezone": "auto",
    }

    try:
        resp = requests.get(base_url, params=params, timeout=30)
        resp.raise_for_status()
        data = resp.json()
    except requests.RequestException:
        return pd.DataFrame(), {
            "avg_temp": np.nan,
            "total_precip": np.nan,
            "max_wind": np.nan,
            "avg_cloud": np.nan,
            "bad_weather_days": 0,
            "weather_score": 50,
            "weather_note": "Weather data could not be retrieved."
        }

    hourly = data.get("hourly", {})
    if not hourly or "time" not in hourly:
        return pd.DataFrame(), {
            "avg_temp": np.nan,
            "total_precip": np.nan,
            "max_wind": np.nan,
            "avg_cloud": np.nan,
            "bad_weather_days": 0,
            "weather_score": 50,
            "weather_note": "No weather data returned."
        }

    hourly_df = pd.DataFrame(hourly)
    summary = summarize_weather(hourly_df)
    summary["weather_note"] = ""
    return hourly_df, summary


